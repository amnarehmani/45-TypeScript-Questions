// Arrays of my dream country list
let dreamCountries = ["Turkey", "South Korea" , "Japan" , "Saudia Arabia" , "Iran"];
console.log("my list" , dreamCountries);

// Alphabetical order
console.log("Alphabetical order:" , [...dreamCountries].sort());

// original order

console.log("original order", dreamCountries);

// Reverse order

console.log("Reverse order", [...dreamCountries].reverse());

// original order

console.log("original order", dreamCountries);

// change order
console.log("original array reversed" , dreamCountries.reverse());

// original order
console.log("Back to original order", dreamCountries.reverse());

// Alphabetical order 
console.log("sorted alphabetical order" , dreamCountries.sort());

// Reversed order again
console.log("reversed order again" , dreamCountries.reverse());